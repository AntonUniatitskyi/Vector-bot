async def generate_post(idea: str, search_results: list[dict]) -> str:
    if search_results:
        sources_block = "\n".join(
            f"- {r['title']}: {r['body']} ({r['href']})" for r in search_results
        )
    else:
        sources_block = "Поиск не дал результатов — используй свои знания, без ссылки на источник."

    prompt = f"""\
Тема запроса от редактора: {idea}

Найденные в интернете материалы:
{sources_block}

Составь пост для канала по этой теме, следуя правилам стиля, разметки и структуры.
"""

    response = await client.aio.models.generate_content(
        model=GEMINI_MODEL,
        contents=prompt,
        config=types.GenerateContentConfig(system_instruction=SYSTEM_PROMPT),
    )
    return response.text


async def rewrite_post(original_text: str) -> str:
    """Просит модель переписать уже готовый черновик другим стилем."""
    prompt = f"""\
Вот черновик поста для Telegram-канала:

{original_text}

Перепиши его: сделай короче, техничнее и динамичнее. Сохрани структуру, \
ссылку на источник (если была) и правила HTML-разметки.
"""

    response = await client.aio.models.generate_content(
        model=GEMINI_MODEL,
        contents=prompt,
        config=types.GenerateContentConfig(system_instruction=SYSTEM_PROMPT),
    )
    return response.text


async def generate_post_stream(idea: str, search_results: list[dict]):
    """
    Потоковая версия generate_post — асинхронный генератор, отдающий
    НАКОПЛЕННЫЙ текст (а не отдельные дельты) по мере генерации.
    Используется для стриминга черновика в Telegram через SendMessageDraft.
    """
    if search_results:
        sources_block = "\n".join(
            f"- {r['title']}: {r['body']} ({r['href']})" for r in search_results
        )
    else:
        sources_block = "Поиск не дал результатов — используй свои знания."

    prompt = f"""\
Тема запроса от редактора: {idea}

Найденные в интернете материалы:
{sources_block}

Составь пост для канала по этой теме, следуя правилам стиля, разметки и структуры.
"""

    stream = await client.aio.models.generate_content_stream(
        model=GEMINI_MODEL,
        contents=prompt,
        config=types.GenerateContentConfig(system_instruction=SYSTEM_PROMPT),
    )

    buffer = ""
    async for chunk in stream:
        if chunk.text:
            buffer += chunk.text
            yield buffer


async def brainstorm_topic(recent_titles: list[str]) -> str:
    avoid_block = "\n".join(f"- {t}" for t in recent_titles) or "(пока нет предыдущих постов)"

    prompt = f"""\
Придумай ОДНУ свежую и интересную тему для следующего поста канала: конкретный \
инструмент, техника, приём OSINT или полезная консольная утилита для линуксоидов \
и пентестеров.

Уже писали про (не повторяй эти темы и близкие к ним):
{avoid_block}

Ответь ТОЛЬКО короткой формулировкой темы (5-10 слов), без разметки и пояснений. \
Например: "OSINT-разведка по email через Have I Been Pwned"
"""

    response = await client.aio.models.generate_content(
        model=GEMINI_MODEL,
        contents=prompt,
        config=types.GenerateContentConfig(system_instruction=SYSTEM_PROMPT),
    )
    return response.text.strip().strip('"')